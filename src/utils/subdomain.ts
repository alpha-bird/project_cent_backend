export const isReserved = (subdomain: string): boolean => {
  return reservedSubdomains.filter(reserved_subdomain =>
    reserved_subdomain.toLowerCase() === subdomain.toLowerCase()).length > 0;
};

export const isValidSubdomain = (subdomain: string): boolean => {
  return subdomain.length > 0 && subdomain.length <= 63 && !/[^a-zA-Z0-9]/.test(subdomain);
};
